function process()
%或加载已经定义好的符号变量
load('matlab.mat')
U11=simplify(expand(y*z*A(1,1,1)+y*(1-z)*A(1,2,1)+(1-y)*z*A(4,1,1)+(1-y)*(1-z)*A(4,2,1)))
U12=simplify(expand(y*z*A(1,1,2)+y*(1-z)*A(1,2,2)+(1-y)*z*A(4,1,2)+(1-y)*(1-z)*A(4,2,2)))
U21=simplify(expand(x*z*A(2,1,1)+x*(1-z)*A(2,2,1)+(1-x)*z*A(2,1,2)+(1-x)*(1-z)*A(2,2,2)))
U22=simplify(expand(x*z*A(5,1,1)+x*(1-z)*A(5,2,1)+(1-x)*z*A(5,1,2)+(1-x)*(1-z)*A(5,2,2)))
U31=simplify(expand(x*y*A(3,1,1)+x*(1-y)*A(6,1,1)+(1-x)*y*A(3,1,2)+(1-x)*(1-y)*A(6,1,2)))
U32=simplify(expand(x*y*A(3,2,1)+x*(1-y)*A(6,2,1)+(1-x)*y*A(3,2,2)+(1-x)*(1-y)*A(6,2,2)))
U1=simplify(expand(x*U11+(1-x)*U12))
U2=simplify(expand(y*U21+(1-y)*U22))
U3=simplify(expand(z*U31+(1-z)*U32))
Fx=simplify(expand(x*(U11-U1)))
Fy=simplify(expand(y*(U21-U2)))
Fz=simplify(expand(z*(U31-U3)))
Fxx=simplify(expand(diff(Fx,x)))
Fxy=simplify(expand(diff(Fx,y)))
Fxz=simplify(expand(diff(Fx,z)))
Fyx=simplify(expand(diff(Fy,x)))
Fyy=simplify(expand(diff(Fy,y)))
Fyz=simplify(expand(diff(Fy,z)))
Fzx=simplify(expand(diff(Fz,x)))
Fzy=simplify(expand(diff(Fz,y)))
Fzz=simplify(expand(diff(Fz,z)))
end